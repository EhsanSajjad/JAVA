package variable;
public class Variable {

    public static void main(String[] args) {
        boolean b = true;
        int i = 317;
        char c = 'a';
        short s = 43;
        float f = 12.5f;
        double d = 12.4;
        
        System.out.println("b = "+b);
        System.out.println("i = "+i);
        System.out.println("c = "+c);
        System.out.println("s = "+s);
        System.out.println("f = "+f);
        System.out.println("d = "+d);
    }
    
}
